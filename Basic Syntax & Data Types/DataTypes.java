public class DataTypes {
    public static void main(String[] args) {
        // Tipe data numerik
        byte angkaByte = 100;
        short angkaShort = 5000;
        int angkaInt = 100000;
        long angkaLong = 15000000000L;

        // Tipe data desimal
        float desimalFloat = 5.75f;
        double desimalDouble = 19.99d;

        // Tipe data karakter
        char karakter = 'A';

        // Tipe data boolean
        boolean benar = true;
        boolean salah = false;

        // Cetak semua variabel
        System.out.println("Byte: " + angkaByte);
        System.out.println("Short: " + angkaShort);
        System.out.println("Int: " + angkaInt);
        System.out.println("Long: " + angkaLong);
        System.out.println("Float: " + desimalFloat);
        System.out.println("Double: " + desimalDouble);
        System.out.println("Char: " + karakter);
        System.out.println("Boolean 1: " + benar);
        System.out.println("Boolean 2: " + salah);
    }
}
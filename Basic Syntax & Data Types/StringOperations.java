public class StringOperations {
    public static void main(String[] args) {
        // Membuat String
        String nama = "Budi";
        String alamat = "Jakarta";

        // Menggabungkan String
        String kalimat = nama + " tinggal di " + alamat;
        System.out.println(kalimat);

        // Operasi String
        String nama1 = "Imoet";
        String nama2 = "Rusdi";
        System.out.println("Sama?: " + nama1.equals(nama2));
    }
}
